// Helper: filter threads by viewer role
// ---------------------------------------------------------------------------

export function getThreadsForRole(
  role: "creator" | "merchant",
  userId: string,
): Thread[] {
  return MOCK_THREADS.filter((t) =>
    t.participants.some(
      (p) => p.role === role && (userId ? p.userId === userId : true),
    ),
  );
}
