// 006_neighborhood_profiles
// ---------------------------------------------------------------------------

/** Neighborhood rollout state. */
export type NeighborhoodStatus = "planning" | "pilot" | "active" | "mature";

/**
 * Per-neighborhood pilot management: targets, current state, pricing tier,
 * seasonality. One row per neighborhood (e.g. "Williamsburg Coffee+").
 * Drives Neighborhood Playbook rollout and the SLR (Software Leverage Ratio)
 * calculation.
 */
export interface NeighborhoodProfile {
  /** Primary key (UUID). */
  id: string;
  /** Human-readable identifier, e.g. "Williamsburg Coffee+". Unique per row. */
  neighborhood_name: string;
  /** Latitude of the neighborhood centroid (-90..90), or `null`. */
  latitude: number | null;
  /** Longitude of the neighborhood centroid (-180..180), or `null`. */
  longitude: number | null;
  /** Target merchant count at maturity. */
  merchants_target: number;
  /** Current onboarded merchant count. */
  merchants_current: number;
  /** Target monthly ARR in USD. */
  monthly_target_arr: number;
  /** Current monthly ARR in USD. */
  monthly_current_arr: number;
  /** Pricing tier label (maps to v5.1 $15-$85/customer band). */
  pricing_tier: string | null;
  /** Forecast multiplier (0..5); 1.0 = flat, >1 = peak season. */
  seasonality_factor: number;
  /** Rollout state. */
  status: NeighborhoodStatus;
  /** ISO timestamp when the row was inserted. */
  created_at: string;
  /** ISO timestamp of the last update. */
  updated_at: string;
}
